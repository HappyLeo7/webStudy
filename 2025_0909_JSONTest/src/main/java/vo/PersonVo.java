package vo;

public class PersonVo {
	/*
	 * "name": "일길동", "age": 21, "tel": "010-111-1234"
	 */
	
	String name;
	int age;
	String tel;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
}
